 <div class="itemdet">
           <img src="/img/type3.jpg">
             <div class="details">
                 <h5 class="namehead"> Red Velvet Cake : </h5>
                 
            <p> This is a true favorite, especially in the south. Made with either butter or oil, red velvet cake's color classically comes from the reaction of buttermilk and cocoa powder. Modern versions often achieve this hue by using red food coloring, or in the case of the raspberry velvet cake, shown here, pink.</p>
                
 
             </div>
         </div>
    <div class="itemdet">
           <img src="/img/type4.jpg">
             <div class="details">
                 <h5 class="namehead"> Carrot Cake : </h5>
                 
            <p> Leavened with baking soda and baking powder, this shortened cake uses oil as its main fat rather than butter. The addition of grated carrots makes the cake extra moist. Carrot cake is also flavored with warm spices and frosted with a rich cream cheese frosting. Pecans or walnuts are optional!</p>
                
 
             </div>
         </div>
    <div class="itemdet">
           <img src="/img/type5.jpg">
             <div class="details">
                 <h5 class="namehead"> Sponge Cake : </h5>
                 
            <p> This is a foam-style cake that has no artificial leaveners (baking powder or baking soda). It gets its volume solely from whipped eggs —either whole eggs or simply whites. This cake takes well to being soaked in a flavored syrup (try soaking with a lemon syrup and serve with a dollop of lemon curd), layered with whipped cream and smashed berries, or if baked up thin, rolled in the Christmas classic, Buche de Noel.</p>
                
 
             </div>
         </div>
    <div class="itemdet">
           <img src="/img/type6.jpg">
             <div class="details">
                 <h5 class="namehead"> Genoise Cake : </h5>
                 
            <p> Genoise is what a sponge cake is called in Italy or France. In this cake, egg yolks, whites and sugar are beaten together until mousse-like. Next, flour, and either oil or butter, are folded in. This type of sponge cake is more moist and tender than its sponge cake cousin.</p>
                
 
             </div>
         </div>
    <div class="itemdet">
           <img src="/img/type7.jpg">
             <div class="details">
                 <h5 class="namehead"> Chiffon Cake : </h5>
                 
            <p> Chiffon cake is a cross between a sponge and oil cake. The addition of oil gives it a richness similar to a shortened cake, and whipped egg whites and baking powder make it light and airy.</p>
                
 
             </div>
         </div>
    <div class="itemdet">
           <img src="/img/type8.jpg">
             <div class="details">
                 <h5 class="namehead"> Angel Food Cake : </h5>
                 
            <p> The lightest of the light, angel food cake is made with only whipped egg whites for leavening and has no added fat. Baked in a special angel food cake pan, it’s often cooled upside down (in the pan) to help maintain the airy texture. The loads of sugar in an angel food cake give it a chewy and sponge-like texture.</p>
                
 
             </div>
         </div>
    <div class="itemdet">
           <img src="/img/type9.jpg">
             <div class="details">
                 <h5 class="namehead"> Flourless Cake : </h5>
                 
            <p> This category includes both baked (think cheese cake or flourless chocolate) or unbaked (like mousse or unbaked cheesecakes) varieties. Typically very rich because of the high fat content, these cakes may or may not have a bottom crumb crust.</p>
                
 
             </div>
         </div>
    <div class="itemdet">
           <img src="/img/type10.jpg">
             <div class="details">
                 <h5 class="namehead"> Upside Down Cake : </h5>
                 
            <p> The classic version of this butter style cake is traditionally made with pineapple, but can be made with many varieties of fruits, including plums, peaches, blueberries, or pears. The toppings are spread on the bottom of the pan, often with butter and sugar, and then topped with cake batter. After cooking, the cake is turned out so that the fruit is facing up.</p>
                
 
             </div>
         </div>
    <div class="itemdet">
           <img src="/img/type11.jpg">
             <div class="details">
                 <h5 class="namehead"> Devil’s Food Cake : </h5>
                 
            <p> This cake is made "devilish" by the addition of chocolate in the form of cocoa powder. Extra baking soda causes the crumb to have more air bubbles, giving it a light and airy texture. This rich cake is frosted with either chocolate frosting or buttercream.</p>
                
 
             </div>
         </div>
    <div class="itemdet">
           <img src="/img/type12.jpg">
             <div class="details">
                 <h5 class="namehead"> Hummingbird Cake : </h5>
                 
            <p> The classic southern oil cake is flavored with banana, pineapple, pecans, lots of warm spices, and topped with a tangy cream cheese frosting. Created in Jamaica and originally called "Doctor Bird Cake,: this confection made its way Stateside sometime in the 1960s.</p>
                
 
             </div>
         </div>
       
    
     <div class="itemdet">
           <img src="/img/type13.jpg">
             <div class="details">
                 <h5 class="namehead"> Opera Cake : </h5>
                 
            <p> The history of this type of cake is a bit muddled. One thing we do know, however, is that its thin layers of coffee-soaked almond cake are sandwiched between an espresso-flavored buttercream, a bittersweet chocolate ganache, and topped off with a second rich chocolate ganache. Light but rich, this pastry is a real showstopper.</p>
                
 
             </div>
         </div>
        <div class="itemdet">
           <img src="/img/type14.jpg">
             <div class="details">
                 <h5 class="namehead"> Lady Baltimore Cake : </h5>
                 
            <p> Fluffy white cake is layered with a fruit and nut filling and topped with a pillowy meringue type frosting. The origin of the cake is somewhat in dispute, but it’s believed to have been created in the southern part of the United States.</p>
                
 
             </div>
